#include<bits/stdc++.h>
using namespace std;

int main(){
    cout << 1 + 1 << endl;
    cout << 3 - 4 << endl;
    cout << 2 * 4 << endl;
    cout << 7 / 3 << endl; // 答えは整数
    cout << 7.0 / 3.0 << endl; // 答えは小数
    cout << 7 % 3 << endl; /* %はあまり */
    cout << 4 % 5 << endl;
    //cout << 3 / 0 << endl; //エラーが出る
}
